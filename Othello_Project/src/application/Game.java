package application;
/**
 * 
 */
public class Game
{
	int whiteScore;
	int blackScore;
	int whiteTime;
	int blackTime;
	
	/**
	 * Default constructor
	 */
	public Game()
	{
		
	}
	
	public int getWhiteScore()
	{
		return whiteScore;
	}
	
	public int getBlackScore()
	{
		return blackScore;
	}
	
	public int getWhiteTime()
	{
		return whiteTime;
	}
	
	public int getBlackTime()
	{
		return blackTime;
	}
	
}